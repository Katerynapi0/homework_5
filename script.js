'use strict'
let userTime = prompt('Введіть кількість годин:');// <-- Запитуємо у користувача скільки годин;
let timeInSeconds = 60 ** 2;// <-- Розрахунок кіл-ті секунд на 1 годину; Був ще варіант з введенням 3600 або 60*60, але на практиці захотілося возвести в ступінь.
let result = userTime * timeInSeconds;// <-- змінна для розрахунку введенної години користувачем помножена на кіл-ть секунд на 1 годину; 

alert(' В ' + userTime + ' год. = ' + result + ' секунд ');//Результат в алерті.